package ex05;

//논리 연산자
public class OperEx03 {
	
	public static void main(String[] args) {
	
		System.out.println(true && false ); // && > 양쪽이 true 값이면 true 출력 아니면 false
		System.out.println(true || false ); 
		System.out.println(false || false ); // || > 양쪽, 또는 둘중 하나의 값만 true면 true, 아니면 false
		
		
		
	}
}
