package ex05;

public class OperEx02 {

	public static void main(String[] args) {

		// 관계 연산자 boolean으로 나타남.
		
		System.out.println(1==1); 
		System.out.println(!(1==1)); // ! : 부정 연산자
		System.out.println(!true);
		
		System.out.println(1!=1); // != : 다른거?
		
		System.out.println(1>3); // 1이 3보다 크냐
		System.out.println(1<3); // 1이 3보다 작냐
		
	}

}
